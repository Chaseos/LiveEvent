package com.hadilq.liveevent.sample

import android.os.Bundle
import android.util.Log
import androidx.appcompat.app.AppCompatActivity
import androidx.lifecycle.Observer
import androidx.lifecycle.ViewModelProviders
import kotlinx.android.synthetic.main.activity_main.*

class MainActivity : AppCompatActivity() {

    private lateinit var viewModel: LiveEventViewModel

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main)

        viewModel = ViewModelProviders.of(this).get(LiveEventViewModel::class.java)

        viewModel.state.observe(this, Observer {
            Log.d("MainActivity", "New click")
            titleView.text = it
        })
        buttonView.setOnClickListener { viewModel.clicked() }
    }
}